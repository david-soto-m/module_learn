'''The four operations functions'''


def add(lhs: float, rhs: float) -> float:
    '''lhs + rhs'''
    return lhs + rhs


def sub(lhs: float, rhs: float) -> float:
    '''lhs - rhs'''
    return lhs - rhs


def mul(lhs: float, rhs: float) -> float:
    '''lhs * rhs'''
    return lhs * rhs


def div(lhs: float, rhs: float) -> float:
    '''lhs / rhs'''
    return lhs / rhs
